let age = "swapnil";  //this is string literal, it is a sequence of characters enclosed in quotes.
let isStudent = true; //this is boolean literal, it represents a logical entity and can have two values: true or false.
let pi = 3.14; //this is numeric literal, it represents a number and can be an integer or a floating-point number.
let nullValue = null; //this is null literal, it represents the intentional absence of any object value.
let undefinedValue; //this is undefined literal, it represents a variable that has been declared but has not been assigned a value.

//typeof operator is used to determine the type of a variable or literal.
console.log(typeof age); // Output: "string"
console.log(typeof isStudent); // Output: "boolean"
console.log(typeof pi); // Output: "number"
console.log(typeof nullValue); // Output: "object" (this is a quirk in JavaScript)
console.log(typeof undefinedValue); // Output: "undefined"