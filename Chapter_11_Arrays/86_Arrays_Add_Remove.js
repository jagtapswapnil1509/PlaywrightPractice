
let arr=[1,2,3];
console.log(arr);

// if you want to add value at the end of array, use Push method

arr.push(4);
console.log(arr);

// if you want to remove value at the end of array, use pop method

arr.pop(4);
console.log(arr);

// pushing of mutltiple elements

arr.push(4,5);
console.log(arr);

// if you want to add value at the beginging of array, use unshift method

arr.unshift(0);
console.log(arr);

//// if you want to remove value at the beginging of array, use shift method

arr.shift();
console.log(arr);

